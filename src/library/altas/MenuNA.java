    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(name.getText().equals("") || country.getText().equals("") || password.getText().equals("")){
            error.setVisible(true);
        }else{
            LibraryBBDD library = new LibraryBBDD();
            List<Author> autores = new ArrayList<>();
            try {
                autores = library.todosAutores();
            } catch (SQLException ex) {
                Logger.getLogger(MenuNA.class.getName()).log(Level.SEVERE, null, ex);
            }
            boolean check = false;
            for(Author key : autores){
                if(key.getName().equals(name.getText())){
                    check = true;
                }
            }
            if(!check){
                int ran = (int) (Math.random()*9999+16);
                Author a = new Author();
                a.setIdauthor(ran);
                a.setName(name.getText());
                a.setSurname(password.getText());
                a.setCountry(country.getText());
                try {
                    library.insertarAutor(a);
                    dispose();
                } catch (SQLException ex) {
                    Logger.getLogger(MenuNA.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

